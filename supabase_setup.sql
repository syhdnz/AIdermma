-- 5. Policies for profiles
CREATE POLICY "Public profiles are viewable by everyone." ON public.profiles
  FOR SELECT USING (true);

CREATE POLICY "Users can insert their own profile." ON public.profiles
  FOR INSERT WITH CHECK (auth.uid()::text = id::text);

CREATE POLICY "Users can update own profile." ON public.profiles
  FOR UPDATE USING (auth.uid()::text = id::text);

-- 6. Policies for scans
-- Note: auth.uid()::text is used because user_id is type TEXT
CREATE POLICY "Users can view their own scans." ON public.scans
  FOR SELECT USING (auth.uid()::text = user_id);

CREATE POLICY "Users can insert their own scans." ON public.scans
  FOR INSERT WITH CHECK (auth.uid()::text = user_id);

CREATE POLICY "Users can delete their own scans." ON public.scans
  FOR DELETE USING (auth.uid()::text = user_id);

-- 7. STORAGE SETUP
-- Create the scans bucket if it doesn't exist
INSERT INTO storage.buckets (id, name, public)
VALUES ('scans', 'scans', true)
ON CONFLICT (id) DO UPDATE SET public = true;

-- Allow authenticated users to upload to the scans bucket
CREATE POLICY "Allow authenticated uploads" ON storage.objects
FOR INSERT TO authenticated WITH CHECK (bucket_id = 'scans');

-- Allow public read access to the scans bucket
CREATE POLICY "Allow public read" ON storage.objects
FOR SELECT TO public USING (bucket_id = 'scans');

-- 8. Trigger for profile creation on signup
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.profiles (id, email, display_name, photo_url)
  VALUES (new.id, new.email, new.raw_user_meta_data->>'full_name', new.raw_user_meta_data->>'avatar_url');
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE OR REPLACE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();
