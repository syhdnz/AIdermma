-- SQL to set up the database for Derma-AI in Supabase

-- 1. Create profiles table (links to auth.users)
CREATE TABLE IF NOT EXISTS public.profiles (
  id UUID REFERENCES auth.users NOT NULL PRIMARY KEY,
  email TEXT NOT NULL,
  display_name TEXT,
  photo_url TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  last_login_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 2. Enable Row Level Security (RLS) on profiles
ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;

-- 3. Create scans table
CREATE TABLE IF NOT EXISTS public.scans (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id TEXT NOT NULL, -- Changed from UUID to TEXT to support Firebase UIDs
  image_url TEXT NOT NULL,
  condition TEXT NOT NULL,
  urgency TEXT NOT NULL,
  confidence TEXT NOT NULL,
  analysis JSONB, -- Added to store the full AI report
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

